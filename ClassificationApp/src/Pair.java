
public class Pair {
	private Integer first;
	private Double second;
	
	public Pair(Integer first, Double second) {
		this.first = first;
		this.second = second;
	}

	public Integer getFirst() {
		return first;
	}

	public void setFirst(Integer first) {
		this.first = first;
	}

	public Double getSecond() {
		return second;
	}

	public void setSecond(Double second) {
		this.second = second;
	}
	
	
}
